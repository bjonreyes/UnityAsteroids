using UnityEngine;
using System.Collections;

public class MenuGameMgr : MonoBehaviour
{
	// ------------------------------------------------------------------------------------------------------------------------------------------
	// UNITY OBJECT PARAMETERS
	// ------------------------------------------------------------------------------------------------------------------------------------------

	// ------------------------------------------------------------------------------------------------------------------------------------------
	// MEMBER CONSTANTS
	// ------------------------------------------------------------------------------------------------------------------------------------------
	const float k_TimeBetweenAsteroids = 2.0f;
	const int k_numMaxAsteroids = 5;
	// ------------------------------------------------------------------------------------------------------------------------------------------
	// MEMBER VARIABLES
	// ------------------------------------------------------------------------------------------------------------------------------------------

	
	// ------------------------------------------------------------------------------------------------------------------------------------------
	// MEMBER METHODS
	// ------------------------------------------------------------------------------------------------------------------------------------------
	
	// ------------------------------------------------------------------------------------------------------------------------------------------
	// Use this for initialization
	// ------------------------------------------------------------------------------------------------------------------------------------------
	void Start () 
	{
		//Set The Num Maximum of Asteroids in The Splash Animation
		AsteroidsMgr.Instance.NumMaximumAsteroids = k_numMaxAsteroids;
		
		//Start Creating Asteroids fot making an animation
		AsteroidsMgr.Instance.StartThrowingAsteroids(k_TimeBetweenAsteroids, false);
	}
	
	// ------------------------------------------------------------------------------------------------------------------------------------------
	// Update is called once per frame
	// ------------------------------------------------------------------------------------------------------------------------------------------
	void Update ()
	{
		
	}
	// ------------------------------------------------------------------------------------------------------------------------------------------
	//  LoadMainLevel: called when we press de button 
	// ------------------------------------------------------------------------------------------------------------------------------------------
	public void LoadMainLevel()
	{
		//Load Main Game Level
		LevelLoader.Instance.LoadLevel(LevelLoader.LevelsToLoad.k_game);
		
		//Stop the Couroutine who creates the asteroids Animation randomly
		AsteroidsMgr.Instance.StopCreateAsteroids();
		
		//Delete all The asteroids in the animation
		AsteroidsMgr.Instance.DeleteAllAsteroidFromList();
	}
	
	
}
